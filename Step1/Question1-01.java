package section1;


public class Question01 {
	//int型変数 n,m を定義し、n=12,m=3として初期化する。
	//n+m, n-m, n×m, n÷m の結果を出力する

	public static void main(String[] args) {

		int n;
		int m;

		n = 12;
		m = 3;

		System.out.println( n + m );
		System.out.println( n - m );
		System.out.println( n * m );
		System.out.println( n / m );
	}

}